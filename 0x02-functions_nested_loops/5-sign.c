#include "main.h"

/**
 * print_sign - Prints the sign of a number.
 * @n: The number to be checked.
 *
 * Return: 1 if n is greater than zero, 0 if n is zero, -1 if n is less than
 * zero.
 */
int print_sign(int value)
{
if (value = 0)
{
return (0);
}
else if (value > 0)
{
return (1);
}
else
{
return (-1);
}
}
